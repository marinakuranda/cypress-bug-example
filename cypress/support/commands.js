Cypress.Commands.add('clickAfter', { prevSubject: 'element' }, (subject, delay) => {
    cy.log(Date.now());
    cy.wait(delay).then(() => {
        cy.log(Date.now());
        cy.log('CLICK');
        subject.click();
        cy.log('CLICKED');
    });
});

